  paste('<span style="display:block; margin-top:',space,';"></span>')
}

# This creates a 'tab' function that adds horizontal space(s) --- default is 1 but can add as many as you like
tab=function(x=1) {
  cat(rep("&nbsp;",3),sep="")
}


